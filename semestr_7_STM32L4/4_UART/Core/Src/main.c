/*
When calling the function, we will pass the latest received byte to it. So we start by checking whether we have accidentally
received an end-of-line character. If so, we need to check if there is already a message in our buffer (the number of bytes is greater than 0).
Earlier in this article, we already mentioned that in the C language there is a character with the code "\0" at the end of the subtitle,
so we add it to our array (and that's why we declared that this array is 1 larger than the maximum expected line length).
We then send our message using printf.*/

/*
On the other hand, if we have received something else from the end-of-line character, we check whether our buffer can still hold further data,
and write the received byte to the nearest free position in the buffer.
As you can see, by using printf, the code of our line_append function has become completely independent of the HAL library or even the microcontroller used.
It is a universal and very simplified code that allows us to process the received characters.
*/

int __io_putchar(int ch)
{
  if (ch == '\n')
  {
    char ch2 = '\r';
    HAL_UART_Transmit(&huart2, &ch2, 1, HAL_MAX_DELAY);
  }
  HAL_UART_Transmit(&huart2, (uint8_t *)&ch, 1, HAL_MAX_DELAY);
  return 1;
}

void line_append(uint8_t value)
{
  if (value == '\r' || value == '\n')
  {
    // we picked up the end-of-line character
    if (line_length > 0)
    {
      // if the buffer is not empty then we add 0 at the end of the line
      line_buffer[line_length] = '\0';
      if (strcmp(line_buffer, "on") == 0)
      {
        HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_SET);
      }
      else if (strcmp(line_buffer, "off") == 0)
      {
        HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_RESET);
      }
      else
      {
        printf("Unknown command %s\n", line_buffer);
      }
      // we process data
      printf("Recived: %s\n", line_buffer);
      // we start the data collection all over again
      line_length = 0;
    }
  }
  else
  {
    if (line_length >= LINE_MAX_LENGTH)
    {
      // too much data, we delete everything we have received so far
      line_length = 0;
    }
    // add the value to the buffer
    line_buffer[line_length++] = value;
  }
}

