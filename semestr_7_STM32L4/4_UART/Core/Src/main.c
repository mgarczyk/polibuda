void turn_LED(uint8_t press_counter){
  if (press_counter >= 10){
    HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_SET);
  }
}

uint8_t when_button_pressed(uint8_t press_counter){
  press_counter++;
  printf("%d\n", press_counter);
  while(is_button_pressed() == true) continue;
  turn_LED(press_counter);
  HAL_Delay(10);
  return press_counter;
} 


